    public void carrgarList(List<Product> prodList) {
        DefaultTableModel tabela = (DefaultTableModel) getListProd().getModel();
        
        tabela.setRowCount(0);
        
        for(Product prod : prodList) {
            tabela.addRow(new Object[]{
                prod.getId(), prod.getNome(), prod.getMark(), prod.getType(), prod.getValidity(), prod.getFabrication(), prod.getValue(), prod.getLote()
            });
        }
    }
    
    public JTable getListProd() {
        return prodList;
    }
    
    
